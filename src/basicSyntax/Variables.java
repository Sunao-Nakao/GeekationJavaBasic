package basicSyntax;

public class Variables {

	public static void main(String[] args) {
		int num1 = 7;
		double num2 = 2.8;
		String favorite = "私の好きな";
		String closingWord = "です。";
		
		System.out.println(num1);
		System.out.println(num2);
		
		System.out.println(favorite + "整数は" + num1 + closingWord);
		System.out.println(favorite + "少数は" + num2 + closingWord);
		
		

	}

}
